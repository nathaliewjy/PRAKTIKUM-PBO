package pertemuan2.models;

public class Mahasiswa {
  public String nim;
  public String nama;
  public String kode_jurusan;

  public Mahasiswa () {
    
  }

  public Mahasiswa(String nim, String nama, String kode_jurusan) {
    this.nim = nim;
    this.nama = nama;
    this.kode_jurusan = kode_jurusan;
  }
}
 