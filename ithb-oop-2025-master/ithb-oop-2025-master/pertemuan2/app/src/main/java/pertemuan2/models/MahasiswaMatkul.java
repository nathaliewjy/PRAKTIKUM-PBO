package pertemuan2.models;

public class MahasiswaMatkul {
    public String nim;
    public String kode_matakuliah;
    public String indeks_nilai;

    public MahasiswaMatkul(String nim, String kode_matakuliah, String indeks_nilai) {
        this.nim = nim;
        this.kode_matakuliah = kode_matakuliah;
        this.indeks_nilai = indeks_nilai;

    }
}
